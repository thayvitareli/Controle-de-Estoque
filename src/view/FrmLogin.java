    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {
        // TODO add your handling code here:

        try {
            String usuario = txtUsuario.getText().toUpperCase();
            String password = txtSenha.getText();
            int find = 0;
            EmployeeDAO obj = new EmployeeDAO();

            for (Employees employee : obj.listarFuncionarios()) {
                if (usuario.equals(employee.getCpf())) {
                    find = 1;
                    if (password.equals(employee.getPassword())) {
                        if (employee.getTipoAcesso().equals("ADMIN")) {
                            JOptionPane.showMessageDialog(null, "Bem vindo ao sistema!\nTipo de acesso: Admin");
                            FrmMenu tela = new FrmMenu();
                            tela.setVisible(true);
                            this.setVisible(false);
                        } else {
                            JOptionPane.showMessageDialog(null, "Bem vindo ao sistema!\nTipo de acesso: Usu√°rio");
                            FrmMenu tela = new FrmMenu();
                            tela.menuCadastroFuncionario.setEnabled(false);
                            tela.menuAtualizarEstoque.setEnabled(false);
                            tela.setVisible(true);
                            this.setVisible(false);
                        }
                    } else {
                        JOptionPane.showMessageDialog(null, "Senha incorreta");
                    }
                }
            }

            if (find == 0) {
                JOptionPane.showMessageDialog(null, "Usu√°rio n√£o cadastrado");
            }
        } catch (Exception error) {
            JOptionPane.showMessageDialog(null, "Erro ao conectar com o banco de dados: " + error);

        }
    }

